package com.paulinavelazquez.composition.code;

import com.paulinavelazquez.composition.Resolution;

public class Monitor {

    private final String model;
    private final String manufacturer;
    private final int size;
    private Resolution nativeResolution;    // This is composition. The monitor is not a resolution, but HAS-A resolution.

    public Monitor(String model, String manufacturer, int size, Resolution nativeResolution) {
        this.model = model;
        this.manufacturer = manufacturer;
        this.size = size;
        this.nativeResolution = nativeResolution;
    }

    public void drawPixelAt(int x, int y, String color) {
        System.out.println("Drawing pixel at " + x
                            + ", " + y + " in color " + color);
    }

    public String getModel() {
        return model;
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public int getSize() {
        return size;
    }

    public Resolution getNativeResolution() {
        return nativeResolution;
    }
}
